<h2 id="fourth">Рестораны</h2>
<p>
  В Сухуме рестораны на любой вкус с абхазской кухней, американским фастфудом,
  японскими блюдами или смешанные, закрытые и с видом на море.
</p>
<h3>Нартаа</h3>
<figure>
  <img src="images/guide-8.jpg" alt="Нартаа" loading="lazy" />
  <figcaption>Нартаа</figcaption>
</figure>
<p>
  Нартаа — ресторан, чтоб познакомиться с местной кухней. Он похож на большой
  абхазский двор из национальных литературных рассказов — это придает ощущения
  нахождения в абхазской гостеприимной семье. В Нарте есть открытые места, где
  дует летний ветер с моря, и закрытые, чтоб спрятаться от дождя.
</p>
<p>
  Основные блюда: хачапур-лодочка, мамалыга, мясо и салаты. В Нарте готовят и
  другие более привычные для туристов блюда, например, пиццу, но смысл в местной
  кухне.
</p>
<h3>Кантри Чикен</h3>
<figure>
  <img src="images/guide-9.jpg" alt="Кантри Чикен" loading="lazy" />
  <figcaption>Кантри Чикен</figcaption>
</figure>
<p>
  Кантри Чикен — замена Макдоналдса и КФС, любимое место молодежи — ходят в
  другие места, но всегда возвращаются в Чикен. Заведение оформлено в
  Австралийском стиле с географическими местами на картах, фотографиями и
  другими атрибутами.
</p>
<p>
  Бесплатный Вайфай и знакомый фастфуд, который часто лучше, чем в популярных
  зарубежных аналогах. Американская кухня с частицей Абхазии.
</p>
<h2 id="fifth">Отели</h2>
<p>
  Мест для остановки в городе много: местные сдают квартиры, номера в
  гостиницах, коттеджи и дома — для удобства туристов. Встречают люди отдыхающих
  со свойственным абхазам гостеприимством. Дешевые уютные квартиры и отели
  класса люкс — всё.
</p>
<h3>Леон</h3>
<figure>
  <img src="images/guide-10.jpg" alt="Отель-ресторан Леон" loading="lazy" />
  <figcaption>Отель-ресторан Леон</figcaption>
</figure>
<p>
  Леон — отель про экономию или дорогой номер люкс. Само здание выглядит
  красиво, сфоткаться не стыдно и есть парковочное место. А рядом находится
  ресторан при отеле, где залы оформлены по-разному: есть и в японском стиле.
</p>
<h3>Рица</h3>
<figure>
  <img src="images/guide-11.jpg" alt="Отель Рица" loading="lazy" />
  <figcaption>Отель Рица</figcaption>
</figure>
<p>
  У Рицы столетняя история — Сухум был знаменит этим заведением, потому что
  здесь любили проводить время Российские и зарубежные знаменитости. Они
  приезжали один раз и продолжали посещать это место.
</p>
<h3>Атриум-Виктория</h3>
<figure>
  <img src="images/guide-12.jpg" alt="Атриум-Виктория" loading="lazy" />
  <figcaption>Атриум-Виктория</figcaption>
</figure>
<p>
  Атриум-Виктория находится в 400 метрах от Сухумской бухты и рядом с центром
  города, значит большинство достопримечательностей в пятнадцати минутах.
  Преимущество — конференц-зал, где предпрениматели и инвесторы проводят свои
  встречи и презентации.
</p>
<p>
  Забронировать номер можно через Букинг или Трипадвайзор.
</p>
<h2 id="sixth">Интересное</h2>
<h3>Колоннада на набережной</h3>
<figure>
  <img src="images/guide-13.jpg" alt="Картины на колоннаде" loading="lazy" />
  <figcaption>Картины на колоннаде</figcaption>
</figure>
<p>
  Колоннада — архитектурная особенность города. Здесь однажды собрались
  Сухумские художники и решили выставлять картины, теперь колоннада — бесплатная
  выставка под небом. Если картина понравилась, можете купить. Большинство
  картин — натюрморты и изображения набережной, но есть абстракции и портреты.
</p>
<h3>Абхазский государственный музей</h3>
<figure>
  <img src="images/guide-14.jpg" alt="Дольмен" loading="lazy" />
  <figcaption>Дольмен</figcaption>
</figure>
<p>
  В Абхазии найдено больше 60 дольменов и один из их расположен в этом музее.
  Дольмены — каменные сооружения из пяти плит, с которыми связаны легенды.
  Строились они в III-II тысячелетиях до нашей эры.
</p>
<p>
  В музее находятся окаменелости, которые изображают древнюю природу Абхазии и
  предметы культуры людей живших до нашей эры и в нашей эре — становится
  понятно, чем занимались люди тогда и как они развивались.
</p>
<h3>Сухумский ботанический сад</h3>
<figure>
  <img src="images/guide-15.jpg" alt="Ботанический сад" loading="lazy" />
  <figcaption>Ботанический сад</figcaption>
</figure>
<p>
  Если с кухней вы уже познакомились, культуру и историю изучили, пора пойти в
  ботанический сад. В саду можно погулять самостоятельно, а можно взять билет с
  экскурсией. В нем находится растительность из Австралии, Южной и Северной
  Америки, Африки, Европы и Азии.
</p>
<h3>Обезьяний питомник</h3>
<figure>
  <img src="images/guide-16.jpg" alt="Обезьяний питомник" loading="lazy" />
  <figcaption>Обезьяний питомник</figcaption>
</figure>
<p>
  Продолжаем экскурсию по природе — теперь фауна — обезьяний питомник. Чтоб
  попасть в питомник надо пройти вершину горы трапеция по лестнице. Его создали
  в конце 20-х годов прошлого века в научных целях — ученые хотели исследовать
  жизнь обезьян в климатических условиях Сухума и Абхазии. Обезьяны хорошо
  прижились, поэтому питомник стал открытым для посещения. Здесь 300 обезьян.
</p>
