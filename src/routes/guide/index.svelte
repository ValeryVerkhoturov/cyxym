  теплый, потому что Кавказские горы сохраняют от плохой погоды и снегопадов, а
  летом прохладно, потому что дует ветер с моря. За цветами можно не идти в сады
  и парки — они везде на улицах.
</p>
<h3>Закат</h3>
<figure>
