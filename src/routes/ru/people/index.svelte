<h2 id="fourth">Традиции</h2>
<figure>
  <img src="images/people-4.jpg" alt="Народные танцы" loading="lazy" />
  <figcaption>Народные танцы</figcaption>
</figure>
<p>
  Традиции абхазов очень интересны и разнообразны. Помимо высокого уважения к
  старшим и младшим существуют и различные другие.
</p>
<p>
  Очень интересно проходит свадебный обряд. Жених тщательно выбирает невесту и
  знакомится, перед окончательным решением о женитьбе посещает ее с посредником,
  который убеждается во взаимной симпатии между ними. После происходит взятие
  невесты, очень интересная часть всего обряда. Взятие сопровождается
  празднованием.
</p>
<p>
  Своё значение имеет и национальная одежда абхазов. Это очень важная атрибутика
  в танцах. Одежда украшает танцы и придаёт им особый характер.
</p>
<p>
  Существуют также традиции, связанные с едой и питьём. Раньше, если приходил
  гость, то хозяин дома посылал ребенка за кувшином с водой. Считалось, чем
  холоднее вода, чем больше она освежает, тем большее уважение проявляет хозяин
  дома к гостю. На стол обязательно ставится лучшая еда, что есть в доме, тем
  самым гостю дают понять, что он почитается в доме.
</p>
<p>
  Жители страны стараются сохранить традиции, чтоб разнообразить историю родины
  и привнести счастья в жизнь.
</p>
<h2 id="fifth">Легенды</h2>
<figure>
  <img src="images/people-5.jpg" alt="Озеро Рица" loading="lazy" />
  <figcaption>Озеро Рица</figcaption>
</figure>
<p>
  Множество легенд существует у абхазов и по сей день. Одной из самых знаменитых
  легенд является сказание о горном озере Рица:
</p>
<p>
  Когда-то на этом месте была долина, и по ней текла река. На сказочных
  пастбищах вдоль берегов ее пасла стадо девушка — красавица по имени Рица,
  единственная сестра трех братьев — Агепсты, Ацетуки и Пшегишхи. Днем братья
  охотились, вечерами собирались у домашнего очага. Рица готовила ужин, братья,
  любуясь ею, пели.
</p>
<p>
  Ушли как-то братья на охоту в дальние горы. Прошел день, второй, они все не
  возвращались. Рица, скучая по ним, все глядела на дорогу и пела своим чарующим
  голосом.
</p>
<p>
  Услышали ее голос два лесных разбойника Гега и Юпшара. Увидев красавицу, они
  решили ее похитить. Юпшара схватил ее и поскакал на своем коне по долине. Гега
  его прикрывал.
</p>
<p>
  Крики о помощи услышали братья. Они помчались вдогонку. Пшегишха метнул в
  разбойников богатырский меч, но промахнулся. Меч упал и перекрыл реку. Вода
  стремительно стала заливать долину и через мгновенье превратила ее в озеро.
</p>
<p>
  Подоспевшая помощь окрылила Рицу, и она из последних сил вырвалась из цепких
  рук разбойников, но, не удержавшись на ногах, упала в бурлящее озеро. Как ни
  старались братья, но спасти сестру не удалось. Рица осталась под водой.
</p>
<p>
  Тогда Пшегишха схватил Юпшару и швырнул его в озеро. Но воды Рицы не приняли
  негодяя и выбросили через меч Пшегишхи и понесли в море. За ним побежал Гега,
  но он не мог спасти друга. Тогда и он присоединился к нему.
</p>
<p>
  От страшного горя братья окаменели и превратились в высокие горы. Стоят они и
  поныне над озером, охраняя покой незабвенной Рицы.
</p>
