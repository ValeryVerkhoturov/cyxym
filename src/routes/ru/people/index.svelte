<h2 id="second">Кухня</h2>
<figure>
  <img src="images/people-2.jpg" alt="Хачапур «Лодочка»" loading="lazy" />
  <figcaption>Хачапур «Лодочка»</figcaption>
</figure>
<p>
  Наша кухня достаточно разнообразна. Основным блюдом на "абхазском столе"
  является мамалыга (на абхазском — абыста). Также, вместе с мамалыгой
  предпочитают есть хачапур. Основными добавками к еде служат асыдзбал, акуд
  (фасоль) и аджика. Так как в Абхазии любят острую еду, то часто добавляют
  аджику. Традиционные блюда можно встретить на столе не только по праздникам,
  но и в любой обычный день. Готовят еду только натуральную, так как семьи имеют
  свое хозяйство.
</p>
<p>
  Интересной частью "абхазской кухни" является апацха. Это место, где группа
  людей может собраться и поесть традиционной еды. Такие места очень популярны
  как у местного населения, так и у приезжих. Мамалыгу варят в котле, иногда на
  глазах у клиентов.
</p>
