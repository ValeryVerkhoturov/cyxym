<h2 id="first">-VI c. — I c.</h2>
<figure>
  <img
    src="images/history-1.jpg"
    alt="The map of -VI c. — I c."
    loading="lazy"
  />
  <figcaption>The map of -VI c. — I c.</figcaption>
</figure>
<p>
  The city of Sukhum is 2500 years old. It is one of the most ancient of the
  cities of the Caucasus. Sukhum was founded, as well as many other cities
  Abkhazia, in the era of Greek colonization (VI century BC). During the period
  of The presence of Greeks in Abkhazia was not the only one of the Abkhazians
  by the people who lived there, these lands were inhabited by different
  nationalities.
</p>
<p>
  Greek colonization was not conquering. The main purpose of the colonization
  was of the Greeks was to establish trade relations with the local citizens.
</p>
<p>
  In the second century B.C. in Asia Minor the Greek the state is the Pontic
  kingdom. The Pontus reaches its highest peak in the King Mithridates. Abkhazia
  is also becoming part of the new state. Mithridates' ambitions to capture the
  entire Black Sea coast lead to to the conflict with the Romans. The defeated
  king runs to the Dioscuriad (Greek name Sukhum). Abkhazia as part of the
  destroyed Pontic kingdom is being taken over by the Romans. The Dioscuriad was
  renamed to Sebastopolis in honor of the Roman Emperor Octavian Augustus
  Sebastos (majestic). To protect their provinces, the Romans are building the
  Pontic Limes — a line of coastal fortifications, part of that were supposed to
  be in Abkhazia.
</p>
<h2 id="second">I c. — VI c.</h2>
<p>
  As a result of the devastating raids of German barbaric tribes and internal
  political problems The Roman Empire is split into West Roman and East Roman
  (Visantia) empires. Abkhazia falls into the sphere of interests of Byzantium
  and Christianity begins to spread actively on its territories. Already by 548
  in the ancient Abkhazian of states - Apsilia and Abasgia - Christianity is
  becoming official religion. However, the acceptance of Christianity does not
  mean loyalty to Byzantium. In 550, when the first Iran-Byzantine war broke
  out, the abasgs and apsils rise up and take the side of Iran. Later, In 555,
  Misimians who did not live in the mountainous regions of Abkhazia rise up. And
  although the rebels have been defeated, the abusers, the apsils and the
  misimians will be long to achieve independence.
</p>
<h2 id="third">VI c. — X c.</h2>
<figure>
  <img
    src="images/images/history-2.jpg"
    alt="The map of VIII c."
    loading="lazy"
  />
  <figcaption>The map of VIII c.</figcaption>
</figure>
<p>
  A prominent figure in the history of unification of Abkhazia is Prince of
  Abasgia Leon I. It is he who is taking the initiative to unite all Abkhazian
  lands into one single Abkhazia. Arabs are beginning to be interested in this
  state. In 737, the Battle of Anakopi, which is important in the history of
  Abkhazia, took place. Arabs lose and are expelled. After that, through
  Dynastic marriage to the daughter of Archil's ally (Georgian prince) Leon I is
  taking possession of new territories.
</p>
<p>
  After the death of Leon I, Leon II began to rule, proclaiming Independence of
  Abkhazia.
</p>
<p>
  The main events of this period in the history of Abkhazia are numerous battles
  for Kartli (one of the Georgian principalities). The struggle for ownership of
  these territories ends with the unification of Georgian of the states with
  Abkhazia into one Abkhaz-Carthwegian state.
</p>
<h2 id="fourth">X c. — XVII c.</h2>
<figure>
  <img
    src="images/history-3.jpg"
    alt="The historical fortress"
    loading="lazy"
  />
  <figcaption>The historical fortress</figcaption>
