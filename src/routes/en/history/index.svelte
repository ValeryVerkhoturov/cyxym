<p>
  In the X-XVII century Tskhum (Sukhum) became an important Black Sea center, as
  well as the summer residence of kings. In Tskhum was spent time the famous
  Queen Tamara. Also in the city in the XIII century were located Genoese
  trading stations. Numerous trade routes appear in Abkhazia. The main interest
  of Genoese is a living commodity - slaves.
</p>
<p>
  Since the 10th century, in Abkhazia ruled by the Bagratid dynasty, which took
  place fortifying Abkhazia in the east.
</p>
<p>
  After the Bagratids Chachba-Shervashidze began to rule.
</p>
<p>
  In the XVI century Abkhazia became a vassal of Mingrelian kingdom Sabediano.
  After repeated armistices Mingrelian principality still decides to capture
  Abkhazia. This does not last long. Soon Abkhazia ...is fortifying itself to
  Sabediano and becoming an independent country.
</p>
<h2 id="fifth">XVII c. — XIX c.</h2>
<figure>
  <img
    src="history-4.jpg"
    alt="Dynasty of Chachba-Shervashidze"
    loading="lazy"
  />
  <figcaption>Dynasty of Chachba-Shervashidze</figcaption>
</figure>
<p>
  However, Abkhazia, as a fully independent country, does not exist for long. In
  the second half of the 17th century, it falls under Turkish control. The city
  Sukhum as a result of this gets a new name — Sukhum-Kale. And for protection
  of their possessions Turks are building a fortress in Abkhazia. And for
  protection of their possessions Turks are building a fortress in Abkhazia. The
  promotion of the Islamic religion begins in Christian Abkhazia. Rulers and the
  feudal lords are the first to adopt Islam. Attempts to Islamize the lower
  classes of society lead to anti-Turkish uprisings in 1725, 1728, 1771.
</p>
<p>
  In 1780s Keleshbei Chachba-Shervashidze became the ruler of Abkhazia, one of
  the most famous rulers of Abkhazia.
</p>
<p>
  At the estuary of the Ingur River on the border of Abkhazia and Megrelia there
  was a controversial Anaklia Fortress. Keleshbei decided to close its question
  once and for all and put 20,000 troops against Megrellia. Mingrelias was in a
  difficult situation, so they conceded Anaklia to Abkhazia. In order to protect
  themselves from the attacks of Keleshbei Megrelia in 1804 comes under the
  auspices of Russia.
</p>
<h2 id="sixth">XIX c. — XX c.</h2>
<figure>
  <img
    src="images/images/history-5.jpg"
    alt="The coast of Abkhazia"
    loading="lazy"
  />
  <figcaption>The coast of Abkhazia</figcaption>
</figure>
<p>
  Keleshbey made his military adventures in Mingreliya when he was a vassal of
  the Ottoman Empire. However, now neither the ruler nor the nobles intended to
  to obey Istanbul. The escalation of relations leads to the military conflict,
  which has led to Abkhazia becoming independent. But, right after achieving
  independence, Abkhazia loses it again. In May 2, 1808 Keleshbei dies and the
  Abkhazian throne is occupied by a puppet from Turkey Aslanbei.
</p>
<p>
  Aslanbei does not hold power for long, and his younger brother George, who was
  in the hands of the Russian Empire, becomes ruler. In 10 June 1810 Russian
  landing party landed in Sukhum-Kale. Abkhazia becomes a vassal Russia.
</p>
<p>
  With the arrival of Russian troops in Abkhazia starts the most tragic page in
  the history of the country — Mahadjirhood. This is a forced displacement of
  the mountainous peoples (mainly Abkhazians, Adygs and Circassians) into the
  Ottoman Turkey.
</p>
<p>
  Waves of Makhajirhood in Abkhazia occurred throughout the XIX century. Total
  there are 10 waves: 1810, 1817, 1824, 1830, 1837, 1840-41, 1855, 1864, 1867,
  1877.
</p>
<p>
  During the whole XIX century, 2 million Adygs and 500 thousand Abkhazians were
  evicted from their native lands to Turkey.
</p>
<p>
  In 1817, Russia's ambitions in the Caucasus were gradually poured into the
  Caucasian war — a clash between Russia and the peoples of the Caucasus.
  Despite the fact that Abkhazia became a part of Russia back in 1810, and there
  were no a single opinion between Abkhazians on which side to go to war on. The
  split of society into pro-Circassian and pro-Russian leads to the separation
  of some Territories from the single Abkhazian principality: Tsebeldinskoye,
  Dalskoye, Psuhvskoye principality and Sadzen (northwest of present-day
  Abkhazia) are opposed to Russia. Abkhazian ruler loses control over mountains
  country's regions. However, these areas have not been kept free for long. From
  1824 up to 1859 there were 5 punitive expeditions of Russian generals to the
  mountainous parts of Abkhazia. Prince Mikhail Chachba himself takes part in
  two of them. As a result of these expeditions, the mountainous principalities
  were defeated and subordinated. There are several waves of Mahadjirhood
  associated with punitive expeditions: mountain villages of Abkhazia were
  burned and their inhabitants were evicted to Turkey.
</p>
<p>
  The Caucasian War ended in 1864 with the victory of Russia. For Abkhazia it
  was meant full obedience to her. In the same year the Abkhazian principality
  was abolished. In its place was created the Sukhum Military Department of the
  Russian Empire. Also, in 1864, there was the 8th wave of Mahadjirhood.
  Abkhazians couldn't live with the Russian order for a long time. Incorrectly
  conducted reform led to the Likhny uprising in 1866. During the
  Russian-Turkish war of 1877-1878, Abkhazians refused to fight against the
  Turks, which is why Sukhum was captured by the Ottomans. And although the
  Turks were soon forced to leave town, before they left, they subjected him to
  ruin.
</p>
<p>
  Dry land lay in ruins, vineyards and gardens were abandoned, livestock were
  slaughtered. Even a huge fountain, a landmark of the city, the Turks took it
  out.
</p>
<h2 id="seventh">XX c.</h2>
<figure>
  <img
    src="images/history-6.jpg"
    alt="The Sukhum railway station"
    loading="lazy"
  />
  <figcaption>The Sukhum railway station</figcaption>
</figure>
<p>
  At the beginning of the 20th century, Abkhazia was, on the whole, an
  undeveloped outskirt of the Russian Empire. Agriculture remained natural. Wine
  making was not withstands competition from foreign producers and therefore
  demand was small. Tobacco growing remained the main industry.
</p>
<p>
  On July 28, 1914 the First World War began. When the German-Turkish warships
  have taken full control of the Black Sea, the position of Abkhazia has become
  a disaster. Government agencies was evacuated from Sukhum, construction on the
  coast has stopped, many businesses have closed down.
</p>
<p>
  In February 1917, a bourgeois-democratic revolution took place in Russia.
</p>
<p>
  In the outskirts of the former empire there was an increase in the number of
  national liberation movements. Abkhazia was no exception. In November 1917
  were established The Abkhazian People's Council (APC) — the first parliament
  in the history of Abkhazia. Simon Basaria became Chairman of the APC. The
  issue of the future of Abkhazia immediately arose in the parliament.
  Bolsheviks headed by Efrem Eshba advocated an alliance with Soviet Russia.
  There was a pro-Georgian faction that pulled Abkhazia into an alliance with
  Georgia. However, the strongest was the group that supported the alliance with
  the Mountainous Republic, which was also established in November 1917.
  Abkhazia became part of the Mountainous Republic. Meanwhile, the Bolsheviks,
  who failed to peacefully draw Abkhazia into an alliance with Soviet Russia,
  planned to turn their plans into reality. On February 18th 1918 ships with
  revolutionary sailors arrived in Sukhum. Bolsheviks seized the city and
  established the Military Revolutionary Committee (MRC) headed by E. Eshba.
  However, the new power lasted for three days: the ANS in an ultimatum form
  demanded the elimination of the Military and Revolutionary Committee. The
  attempt to seize power failed. But in the April 8, 1918, a second attempt took
  place, which was crowned with success.
</p>
<p>
  The consequences of the establishment of the conscience of power were
  terrible. Georgia took advantage of the fact that the Menshevik Abkhazian
  authorities, with which the treaty relations were signed, fell, and brought
  its troops into the country. On May 17th the troops of Georgian General Jugeli
  entered Sukhum with heavy fighting. The second Soviet power fell. After the
  fall of Soviet power the second ANS was formed, which at the Batumi Peace
  Conference managed to get Georgian troops out of Abkhazia. However, as early
  as June 1918 under the pretext of "establishment of firm power and assistance
  of the APS" Georgian troops headed by General Maznikov occupied Abkhazia
  again.
</p>
<p>
  In 1921, rebel groups with the support of the 9th Red Army opposed Georgia. On
  March 10, 1921, the Georgian Mensheviks were expelled from Abkhazia. The
  country established Soviet power.
</p>
<p>
  In 1926, industrialization and collectivization began in Abkhazia. "In 1935,
  the first Tkvarcheli coal mine was put into operation. Construction of the
  Sukhum HPP has begun."
</p>
<p>
  In Soviet times, the city of Sukhum was improved. The following institutions
  were built: Art School, Institute of Subtropical Economy and many others.
</p>
<p>
  Active development of tourism, which brought considerable income to the
  Abkhazian ASSR, took place.
</p>
<p>
  During the Great Patriotic War cadets of the Sukhum School took part in the
  battles for the Klokhori mountain pass during the Battle of the Caucasus.
  After Stalin's death, the Abkhazian language is being translated from the
  Georgian graphic base into the Cyrillic alphabet. Abkhazian State University
  (ASU) was opened in Sukhum.
</p>
<p>
  1989 is considered the year of the beginning of the Georgian-Abkhaz conflict.
  This year in Sukhum occurs clashes between Abkhazians and Georgians with huge
  losses for both sides. The USSR Ministry of Internal Affairs had little
  control over the situation, and after the collapse of the Soviet Union in 1991
  the situation became critical. The situation was complicated by the beginning
  of the civil war in Georgia, when zviadists — supporters of the deposed
  president — began to hold rallies in Sukhum in support of him. All this caused
  discontent among the local population. It is also worth noting that the USSR
  left Abkhazia as an independent republic. In 1990, the Abkhazian SSR separated
  from the Georgian SSR. On August 14th 1992, the State Council of Georgia
  introduced troops into Abkhazia to prevent its final secession.
</p>
<p>
  The Patriotic War of Abkhazia begins. Abkhazia did not have a regular army and
  Ministry of Defense. Sukhum was occupied by the Georgian troops almost without
  a fight, but that was the end of the victory of the State Council. On November
  29th Abkhazian militia managed to push enemy troops southward during the
  Kocharian offensive. On October 6th Abkhazian troops are carrying out the
  Storm operation and destroying the landing of the State Council in Gagra. On
  July 2, 1993, Operation Storm 777 or Tamysh operation took place, which was a
  turning point in the war. On September 27th 1993 during the September
  offensive Sukhumi was liberated, and then the whole of Abkhazia was liberated.
</p>
<p>
  The war has caused great damage to the economy of Abkhazia. In the territories
  occupied by the Georgian army, genocide was carried out: ASU, scientific
  research institutes, Sukhum Physical-Technical Institute were destroyed. The
  building with archives and libraries were burnt down together with books that
  were there. Hundreds thousands of unique documents were destroyed.
</p>
